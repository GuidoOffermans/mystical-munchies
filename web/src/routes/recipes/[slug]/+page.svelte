<ul class="mt-4">
	<li>butter, for the tin</li>
	<li>100g white chocolate</li>
	<li>200g digestive biscuits, crushed</li>
	<li>500g mascarpone</li>
	<li>3 eggs</li>
	<li>100g caster sugar</li>
	<li>1 lemon, zested, 1/2 juiced</li>
	<li>½ tsp vanilla bean paste</li>
	<li>50g shelled pistachios, roughly chopped</li>
	<li>1 tbsp icing sugar (optional)</li>
	<li>mixed berries, to serve (optional)</li>
</ul>

<div class="mt-4" />

<h4 class="h4">STEP 1</h4>
<p>
	Line the base of a deep 20cm springform cake tin with baking parchment and butter the base and
	sides. Melt the white chocolate in a large heatproof bowl set over a pan of just simmering water
	(ensure the bottom of the bowl doesn’t touch the water). Alternatively, melt the chocolate in a
	microwave in short bursts. When the chocolate is completely smooth, stir in the biscuits and a
	small pinch of salt. Press into the base of the tin, all the way to the edges, ensuring the base
	is even. Put in the fridge to chill for at least 20 mins.
</p>

<h4 class="h4 mt-2">STEP 2</h4>
<p>
	Heat the oven to 140C/120C fan/gas 1. Beat the mascarpone, yogurt, eggs, caster sugar, lemon
	zest and juice and vanilla bean paste together with an electric whisk until smooth, then tip
	over the biscuit base. Transfer the tin to a baking tray and bake for 45 mins until almost set,
	with just a slight wobble in the middle. Turn the oven off with the cheesecake inside, and leave
	the door ajar. Leave the cheesecake to cool completely.
</p>

<h4 class="h4 mt-2">STEP 3</h4>
<p>
	Once completely cool and set, loosen the sides and base of the tin with a knife, and carefully
	transfer the cheesecake to a serving plate. Scatter over the pistachios and dust with the icing
	sugar, then serve with the berries on the side.
</p>
